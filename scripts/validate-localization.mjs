/**
 * validate-localization.mjs
 *
 * Detects STALE localized fields by fingerprinting the English source each
 * translation derives from. Fingerprints live in a separate lockfile
 * (data/localization-baseline.json) so the translator-facing data files stay
 * pristine. When the master changes, a field's hash no longer matches its
 * baseline and the field is flagged for retranslation.
 *
 * The dependency is PER-FIELD — and notably NOT all on the English name:
 *   localized.name        <- master.name
 *   localized.aliases     <- master.aliases
 *   localized.dictation   <- master.unicodemath   (faithful to the MATH, not the name —
 *                                                   so renaming an equation does NOT stale it)
 *   localized.description <- master.description    (when a localized description exists)
 *
 * Modes:
 *   (default)   report stale / untracked / orphan findings; exit 0
 *   --check     same report; exit 1 if any finding (for npm scripts / CI)
 *   --bless     rewrite the baseline lockfile to the current source hashes. Run
 *               AFTER a (re)translation pass to mark everything current.
 * Scope (report/bless): --locale es   --id 1019
 *
 * Usage: node scripts/validate-localization.mjs [--check | --bless] [--locale xx] [--id N]
 */

import { readFileSync, writeFileSync, readdirSync, existsSync } from 'fs';
import { createHash } from 'crypto';
import { dirname, resolve } from 'path';
import { fileURLToPath } from 'url';

const __dir = dirname(fileURLToPath(import.meta.url));
const DATA = resolve(__dir, '../data');
const LOCK = resolve(DATA, 'localization-baseline.json');

const args = process.argv.slice(2);
const has = (f) => args.includes(f);
const opt = (f) => { const i = args.indexOf(f); return i >= 0 ? args[i + 1] : undefined; };
const MODE = has('--bless') ? 'bless' : has('--check') ? 'check' : 'report';
const onlyLocale = opt('--locale');
const onlyId = opt('--id') !== undefined ? Number(opt('--id')) : undefined;

// Short, stable content fingerprint.
const fp = (v) => createHash('sha256')
  .update(typeof v === 'string' ? v : JSON.stringify(v ?? null))
  .digest('hex').slice(0, 10);

// localized field -> the master source value it is a translation of.
const SOURCE = {
  name:        (m) => m.name,
  aliases:     (m) => m.aliases ?? [],
  dictation:   (m) => m.unicodemath,   // dictation tracks the math structure, not the EN text
  description: (m) => m.description,
};

const master = JSON.parse(readFileSync(resolve(DATA, 'named-equations.json'), 'utf8'));
const byId = new Map(master.equations.map((e) => [e.id, e]));

const locFiles = readdirSync(DATA)
  .filter((f) => /^named-equations\.[a-z]{2}\.json$/.test(f))
  .sort();

const baseline = existsSync(LOCK) ? JSON.parse(readFileSync(LOCK, 'utf8')) : {};

const findings = [];
let stale = 0, untracked = 0, orphan = 0;
const coverage = [];
const fresh = {};   // rebuilt baseline (for --bless)

for (const file of locFiles) {
  const locale = file.match(/\.([a-z]{2})\.json$/)[1];
  const loc = JSON.parse(readFileSync(resolve(DATA, file), 'utf8'));
  if (!onlyLocale || locale === onlyLocale) {
    coverage.push(`${locale}: ${loc.equations.length}/${master.equations.length} equations localized`);
  }
  fresh[locale] = {};

  for (const e of loc.equations) {
    const m = byId.get(e.id);
    // Only the fields actually present in the localized entry (i.e. translated).
    const fields = m ? Object.keys(SOURCE).filter((f) => f in e && SOURCE[f](m) !== undefined) : [];
    if (m) { fresh[locale][e.id] = {}; for (const f of fields) fresh[locale][e.id][f] = fp(SOURCE[f](m)); }

    const inScope = (!onlyLocale || locale === onlyLocale) && (onlyId === undefined || e.id === onlyId);
    if (!inScope || MODE === 'bless') continue;

    if (!m) { orphan++; findings.push(`[orphan]    ${locale} #${e.id} — id not in master`); continue; }
    const base = baseline[locale]?.[e.id] ?? {};
    for (const f of fields) {
      const cur = fp(SOURCE[f](m));
      if (!(f in base)) {
        untracked++;
        findings.push(`[untracked] ${locale} #${e.id} ${f} — no baseline (run --bless)`);
      } else if (base[f] !== cur) {
        stale++;
        findings.push(`[STALE]     ${locale} #${e.id} ${f} — EN ${f === 'dictation' ? 'unicodemath' : f} changed; retranslate`);
      }
    }
  }
}

if (MODE === 'bless') {
  // Preserve baseline for locales not regenerated this run (when --locale is scoped).
  const out = onlyLocale ? { ...baseline, [onlyLocale]: fresh[onlyLocale] ?? {} } : fresh;
  const header = {
    _comment: 'GENERATED by scripts/validate-localization.mjs --bless. Per-locale source fingerprints (locale -> equation id -> field -> hash of the EN source) used to flag stale translations. Do not edit by hand. Re-bless after a (re)translation pass.',
  };
  writeFileSync(LOCK, JSON.stringify({ ...header, ...out }, null, 2) + '\n', 'utf8');
  console.log(`✓ wrote ${LOCK.replace(DATA + '\\', '').replace(DATA + '/', '')}`);
  process.exit(0);
}

console.log('=== Localization staleness ===');
coverage.forEach((c) => console.log('  ' + c));
console.log('');
if (findings.length) findings.forEach((f) => console.log('  ' + f));
else console.log('  ✓ all tracked translations are current');
console.log(`\n  ${stale} stale, ${untracked} untracked, ${orphan} orphan`);

if (MODE === 'check' && (stale || untracked || orphan)) process.exit(1);
process.exit(0);
